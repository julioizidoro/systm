    public void listarDestinos() {
        NotificacaoEmailController notificacaoEmailController = new NotificacaoEmailController();
        String tipo = "";
        if (tipoAviso.equalsIgnoreCase("Inclusão")) {
            tipo = "A";
        } else {
            tipo = "I";
        }
        List<Viewnotificacaoemail> listaNotificacao = notificacaoEmailController.listar(idProduto, tipo);
        if (listaNotificacao != null) {
            for (int i = 0; i < listaNotificacao.size(); i++) {
                try {
                    email.addTo(listaNotificacao.get(i).getEmail());
                } catch (EmailException ex) {
                    Logger.getLogger(EnviarEmailBean.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

