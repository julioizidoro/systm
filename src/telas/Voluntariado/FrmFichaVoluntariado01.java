            boolean resultado = JOptionPane.showConfirmDialog(null, "Taxa TM nÃ£o pode ser ExcluÃ­da. Alterar o valor da Taxa TM?", "AlteraÃ§Ã£o", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0;
