    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (usuario==null){
            usuario = new Usuario();
            String senha = Formatacao.SenhaString(senhajPasswordField.getPassword());
            try {
                usuario.setSenha(Criptografia.encript(senha));
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(FrmCadastrarUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane, "Senha gerada = senha" );
        }
        usuario.setNome(nomejTextField.getText());
        usuario.setLogin(loginjTextField.getText());
        usuario.setSituacao("Ativo");
        usuario.setCargo(cargojTextField.getText());
        usuario.setEmail(emailjTextField.getText());
        usuario.setTipo(tipojComboBox.getSelectedItem().toString());
        Unidadenegocio unidadenegocio = (Unidadenegocio) unidadeNegociojComboBox.getSelectedItem();
        if(unidadenegocio!=null){
            usuario.setUnidadenegocio(unidadenegocio.getIdunidadeNegocio());
        }
        Grupoacesso grupo = (Grupoacesso) grupoAcessojComboBox.getSelectedItem();
        usuario.setGrupoacesso(grupo.getIdgrupoAcesso());
        UsuarioController usuarioController = new UsuarioController();
        usuario = usuarioController.salvar(usuario);
        this.dispose();
        
    }//GEN-LAST:event_jButton1ActionPerformed

