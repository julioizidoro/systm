    public void preencherDadosUsuario(){
        nomejTextField.setText(usuario.getNome());
        loginjTextField.setText(usuario.getLogin());
        tipojComboBox.setSelectedItem(usuario.getTipo());
        emailjTextField.setText(usuario.getEmail());
        cargojTextField.setText(usuario.getCargo());
        UnidadeNegocioController unidadeNegocioController = new UnidadeNegocioController();
        Unidadenegocio unidadenegocio = unidadeNegocioController.consultar(usuario.getUnidadenegocio());
        if (unidadenegocio!=null){
            unidadeNegociojComboBox.setSelectedItem(unidadenegocio);
        }
        GrupoAcessoController grupoAcessoController = new GrupoAcessoController();
        Grupoacesso grupo = grupoAcessoController.consultar(usuario.getGrupoacesso());
        if(grupo!=null){
            grupoAcessojComboBox.setSelectedItem(grupo);
        }
                
    }
    
    
    
    public void peencherComboboxUnidadenegocio(){
        UnidadeNegocioController unidadeNegocioController = new UnidadeNegocioController();
        List<Unidadenegocio>  listaUnidadeNegocio = unidadeNegocioController.listar();
        if (listaUnidadeNegocio==null){
            listaUnidadeNegocio = new ArrayList<Unidadenegocio>();
        }
        unidadeNegociojComboBox = Formatacao.preencherComobox(listaUnidadeNegocio, unidadeNegociojComboBox, false, null);
    }
    
    public void preencherComboBoxGrupoACesso(){
        GrupoAcessoController grupoAcessoController = new GrupoAcessoController();
        List<Grupoacesso> listaGrupo = grupoAcessoController.listar();
        if (listaGrupo!=null){
            for(int i=0;i<listaGrupo.size();i++){
                grupoAcessojComboBox.addItem(listaGrupo.get(i));
            }
        }
    }
    public void limitarJText(){
        nomejTextField.setDocument(new LimiteTextoJedit(100));
        emailjTextField.setDocument(new LimiteTextoJedit(100));       
        cargojTextField.setDocument(new LimiteTextoJedit(50));        
        loginjTextField.setDocument(new LimiteTextoJedit(30));                   
    }
