    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        Usuario usuario = usuarioLogadoBean.getUsuario();
        UsuarioController usuarioController = new UsuarioController();
        usuario.setLocalsalvar(localSalvarjTextField.getText());
        usuario.setLocarlvisualizar(localVisualizarjTextField.getText());
        usuario = usuarioController.salvar(usuario);
        usuarioLogadoBean.setUsuario(usuario);
        JOptionPane.showMessageDialog(rootPane, "Local padrão salvo sucesso");
        this.dispose();
    }//GEN-LAST:event_salvarjButtonActionPerformed

