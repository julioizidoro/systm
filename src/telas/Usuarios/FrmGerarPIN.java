    public void carregarUnidade(){
        UnidadeNegocioController unidadeNegocioController = new UnidadeNegocioController();
        List<Unidadenegocio> lista = unidadeNegocioController.listar();
        unidadeNegociojComboBox = Formatacao.preencherComobox(lista, unidadeNegociojComboBox, true, "Selecione Unidade");
    }
    
    public void carregarUsuario(int idUinidade){
        UsuarioController usuarioController = new UsuarioController();
        List<Usuario> lista = usuarioController.listaUsuarioUnidade(idUinidade);
        usuariojComboBox.removeAllItems();
        usuariojComboBox = Formatacao.preencherComobox(lista, usuariojComboBox, true, "Selecione Usuário");
    }
    

