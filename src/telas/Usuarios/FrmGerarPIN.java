    private void unidadeNegociojComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_unidadeNegociojComboBoxItemStateChanged
        Object objeto = unidadeNegociojComboBox.getSelectedItem();
        if (objeto instanceof Unidadenegocio) {
            Unidadenegocio unidade = (Unidadenegocio) unidadeNegociojComboBox.getSelectedItem();
            if (unidade != null) {
                carregarUsuario(unidade.getIdunidadeNegocio());
            }
        }
    }//GEN-LAST:event_unidadeNegociojComboBoxItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Unidadenegocio unidade = (Unidadenegocio) unidadeNegociojComboBox.getSelectedItem();
        Usuario usuario = (Usuario) usuariojComboBox.getSelectedItem();
        boolean gerar =  true;
        if (usuario==null){
            gerar = false;
        }
        if (unidade==null){
            gerar = false;
        }
        if (gerar){
            Random random = new Random();
            int x = random.nextInt(1000);
            x = x + 1;
            int npin = usuario.getIdusuario() + 10 + unidade.getIdunidadeNegocio() + 15 + x;
            String sPin =  String.valueOf(npin);
            Pincambio pinCamio = new Pincambio();
            pinCamio.setConsultor(usuario.getIdusuario());
            pinCamio.setDataEmissao(new Date());
            pinCamio.setUtilizado("N");
            pinCamio.setNumero(sPin);
            pinCamio.setUsuario(usuarioLogadoBean.getUsuario());
            UsuarioController usuarioController = new UsuarioController();
            pinCamio = usuarioController.salvar(pinCamio);
            pinjLabel.setText(sPin);
            Formatacao f = new Formatacao();
            String texto = "Segue numério de PIN para fazer alteração de câmbio\r\n";
            texto = texto + "\r\n";
            texto = texto + "\r\n";
            texto = texto + "\r\n";
            texto = texto + "Nº PIN ===>" + sPin +"\r\n";
            f.enviarEmailPINCambio(texto, usuario.getEmail());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

