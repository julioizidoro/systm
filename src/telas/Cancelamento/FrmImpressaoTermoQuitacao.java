    public void imprimirRelatorioTermoQuitacao(){
        String url = ("telas/Cancelamento/Relatórios/reciboTermoQuitacao.jasper");
            Map parameters = new HashMap();
            try {
                parameters.put("banco", bancojTextField1.getText());
                parameters.put("agencia", agenciajTextField2.getText());
                parameters.put("conta", contajTextField3.getText());
                parameters.put("titular", titularjTextField4.getText());
                parameters.put("valor", valorReembolsojTextField5.getText());
                parameters.put("idvendas", idCliente);
                parameters.put("programa", curso);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Não foi possível gerar o relatório " + ex);
                ex.printStackTrace();
            }
            new relatoriosJasper(url, parameters);
    }
