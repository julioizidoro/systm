    private void valorLojajTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorLojajTextFieldKeyReleased
        float valorMulta = 0.0f;
        if (valormultajTextField.getText().length()>0){
            valorMulta = Formatacao.formatarStringfloat(valormultajTextField.getText());
        }
        float valorLoja = 0.0f;
        if (valorLojajTextField.getText().length()>0){
            valorLoja = Formatacao.formatarStringfloat(valorLojajTextField.getText());
        }
        float valorMatriz = Formatacao.formatarStringfloat(valorMatrizjTextField.getText());
        float total = (valorMatriz + valorLoja) - valorMulta;
        valorPagarjTextField.setText(Formatacao.formatarFloatString(total));
        if (total<=0){
            JOptionPane.showMessageDialog(rootPane, "Erro Total menor ou igual a 0");
        }
    }//GEN-LAST:event_valorLojajTextFieldKeyReleased

