    public Float calcaularValorRecebidoMatriz(){
        ContasReceberController contasReceberController = new ContasReceberController();
        List<Contasreceber> lista = contasReceberController.listar(idVenda);
        float soma = 0.0f;
        if (lista!=null){
            for(int i=0;i<lista.size();i++){
                if (lista.get(i).getValorpago()>0){
                    soma+=lista.get(i).getValorparcela();
                }
            }
        }
        return soma;
    }


