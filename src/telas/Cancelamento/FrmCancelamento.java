    private void confirmajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmajButtonActionPerformed
        cancelamento.setDataacerto(dataAcertojDateChooser.getDate());
        cancelamento.setDatasolicitacao(dataSolicitacaojDateChooser.getDate());
        if (realizadoLojajRadioButton.isSelected()){
            cancelamento.setLocalacerto("Realizado na Loja");
        }else {
            cancelamento.setLocalacerto("Realizado na Matriz");
        }
        if (reembolsojRadioButton.isSelected()){
            cancelamento.setTipopagamento("Reembolso");
        }else {
            cancelamento.setTipopagamento("Crédito");
        }
        if (valorMatrizjTextField.getText().length()>0){
            cancelamento.setValormatriz(Formatacao.formatarStringfloat(valorMatrizjTextField.getText()));
        }else {
            cancelamento.setValormatriz(0.0f);
        }
        if (valorLojajTextField.getText().length()>0){
            cancelamento.setValorloja(Formatacao.formatarStringfloat(valorLojajTextField.getText()));
        }else {
            cancelamento.setValorloja(0.0f);
        }
        if (valormultajTextField.getText().length()>0){
            cancelamento.setMulta(Formatacao.formatarStringfloat(valormultajTextField.getText()));
        }else {
            cancelamento.setMulta(0.0f);
        }
        if (valorPagarjTextField.getText().length()>0){
            cancelamento.setValorreembolso(Formatacao.formatarStringfloat(valorPagarjTextField.getText()));
        }else {
            cancelamento.setValorreembolso(0.0f);
        }
        cancelamento.setUsuario(usuarioLogadoBean.getUsuario().getIdusuario());
        cancelamento.setVendas(idVenda);
        cancelamento.setObservacao(observacaojTextArea.getText());
        CancelamentoController cancelamentoController = new CancelamentoController();
        cancelamento = cancelamentoController.salvar(cancelamento);
        JOptionPane.showMessageDialog(rootPane, "Cancelamento efetuado com Sucesso");
        this.dispose();
    }//GEN-LAST:event_confirmajButtonActionPerformed

