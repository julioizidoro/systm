    public void carregarValorTrainee() {
        if (valoresTrainee != null) {
            destinoValorjTextField.setText(valoresTrainee.getFornecedorcidade().getCidade().getPais().getNome());
            if (valorCambio > 0) {
                if (listaProdutoOrcamentoBean == null) {
                    listaProdutoOrcamentoBean = new ArrayList<ProdutoOrcamentoBean>();
                }
                apagarProduto();
                float valorEstrangeira = valoresTrainee.getValorgross();
                float valorReal = valoresTrainee.getValorgross() * cambio.getValor();
                if (valoresTrainee.getProdutosorcamento() != null) {
                    ProdutoOrcamentoBean pob = new ProdutoOrcamentoBean();
                    pob.setIdOrcamentoProdutoOrcamento(0);
                    pob.setDescricaoProdutoOrcamento(valoresTrainee.getProdutosorcamento().getDescricao());
                    pob.setIdProdutoOrcamento(valoresTrainee.getProdutosorcamento().getIdprodutosOrcamento());
                    pob.setValorMoedaEstrangeira(valorEstrangeira);
                    pob.setValorMoedaReal(valorReal);
                    pob.setApagar(false);
                    pob.setTipo("A");
                    pob.setVisto(false);
                    pob.setSeguro(false);
                    pob.setNovo(true);
                    listaProdutoOrcamentoBean.add(pob);
                    calcularValorTotalOrcamento();
                    carregarModelOrcamentoProduto();
                    valorProdutosMoedaEstrangeirajTextField.setText("");
                    valorProdutosMoedaRealjTextField.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Cambio não selecionado");
            }
        }
    }
    
