    public void verifcarEmbarcados(){
        if (usuarioLogadoBean.getUsuario().getTipo().equalsIgnoreCase("Gerencial")){
            if (usuarioLogadoBean.getUnidadeNegocio().getDataembarcadocurso()!=null){
                String dataUnidade = Formatacao.ConvercaoDataPadrao(usuarioLogadoBean.getUnidadeNegocio().getDataembarcadocurso());
                String dataSistema = Formatacao.ConvercaoDataPadrao(new Date());
                if (!dataUnidade.equalsIgnoreCase(dataSistema)){
                    TraineeController traineeControllerController = new TraineeController();
                    //traineeControllerController.gerarSitaucaoEmbarcado();
//                    gerarFinalizados();
                    UnidadeNegocioController unidadeNegocioController = new UnidadeNegocioController();
                    Unidadenegocio unidadeNegocio = usuarioLogadoBean.getUnidadeNegocio();
                    unidadeNegocio.setDataembarcadocurso(new Date());
                    unidadeNegocio = unidadeNegocioController.salvar(unidadeNegocio);
                    usuarioLogadoBean.setUnidadeNegocio(unidadeNegocio);
                    JOptionPane.showMessageDialog(rootPane, "Embarques Verificados com Sucesso");
                }
            }
        }
    }
    
