    private void imprimirjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imprimirjButton2ActionPerformed
        if (usuarioLogadoBean.getAcesso().getPccfinalizar() == 1) {
            boolean resultado = JOptionPane.showConfirmDialog(null, "Finalizar Processo?", "Finalizar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0;
            if (resultado) {
                int linha = vendasCursojTable.getSelectedRow();
                if (linha >= 0) {
                    TraineeController traineeController = new TraineeController();
                    Trainee  trainee = traineeController.consultar(listaVendas.get(linha).getIdvendas());
                    if (trainee != null) {
                        trainee.setControle("Finalizado");
                        traineeController.salvar(trainee);

                    }
                    Controletrainee controle = traineeController.consultarControleTrainee(listaVendas.get(linha).getIdvendas(), listaVendas.get(linha).getIdcontroletrainee());
                    if (controle != null) {
                        controle.setSituacao("Finalizado");
                        traineeController.salvar(controle);
                    }
                    carregarModelVendas();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Acesso Negado");
            }
        }
    }//GEN-LAST:event_imprimirjButton2ActionPerformed

