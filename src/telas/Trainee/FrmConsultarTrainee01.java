    private void EditarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarjButtonActionPerformed
        if (usuarioLogadoBean.getAcesso().getCursosEditar() == 1) {
            int linha = vendasCursojTable.getSelectedRow();
            if (linha >= 0) {
                new FrmFichaTrainee(listaVendas.get(linha).getIdvendas(), usuarioLogadoBean, this, listaCambio.get(0).getData());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Acesso Negado");
        }
    }//GEN-LAST:event_EditarjButtonActionPerformed

    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        int linha = vendasCursojTable.getSelectedRow();
        if (linha>=0){
            boolean resultado = JOptionPane.showConfirmDialog(null, "Confirma Cancelamento?", "Cancelar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0;
            if (resultado) {
                int idCliente = listaVendas.get(linha).getIdcliente();
                int idvenda = listaVendas.get(linha).getIdvendas();
                String curso = listaVendas.get(linha).getProgramaReponsavelEUA();
                String motivo = JOptionPane.showInputDialog("Motivo do Cancelamento");
                CancelarVenda(listaVendas.get(linha).getIdvendas(), listaVendas.get(linha).getNomeCliente(), Formatacao.ConvercaoDataPadrao(listaVendas.get(linha).getPrimeiraDataChegada()), motivo);
                JOptionPane.showMessageDialog(rootPane, "Ficha de Traanie Cancelada");
                new FrmCancelamento(usuarioLogadoBean, idvenda, idCliente, curso);
                carregarModelVendas();
            }
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

