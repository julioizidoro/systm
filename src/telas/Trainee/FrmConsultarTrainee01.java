    public void excluirCursos(int idVenda){
        CursosController cursosController = new CursosController();
        Cursos curso = cursosController.consultarCursos(idVenda);
        if (curso!=null){
            List<Controlecursos> listacontrolecursos = cursosController.consultarControleCursosList(idVenda);
            if (listacontrolecursos!=null){
                for(int i=0;i<listacontrolecursos.size();i++){
                    cursosController.excluirControleCurso(listacontrolecursos.get(i));
                }
                
            }
            cursosController.excluir(curso.getIdcursos());
        }
    }
    
   

