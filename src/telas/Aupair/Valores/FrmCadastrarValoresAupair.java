    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String msg = validarDados();
        if (msg.length() < 5) {
            valoresAupair.setDescricao(duracaojTextField.getText());
            Moedas moedaVenda = (Moedas) moedasVendasjComboBox.getSelectedItem();
            valoresAupair.setMoedas(moedaVenda);
            Moedas moedaComissao = (Moedas) moedasComissaojComboBox.getSelectedItem();
            valoresAupair.setMoedas1(moedaComissao);
            valoresAupair.setFornecedor(fornecedorCidade.getFornecedor());
            valoresAupair.setFornecedorcidade(fornecedorCidade);
            valoresAupair.setValornet(Formatacao.formatarStringfloat(valorNetjTextField.getText()));
            valoresAupair.setValorgross(Formatacao.formatarStringfloat(valorgrossjTextField.getText()));
            valoresAupair.setComissaopremium(Formatacao.formatarStringfloat(valorPremiumjTextField.getText()));
            valoresAupair.setComissaoexpress(Formatacao.formatarStringfloat(valorExpressjTextField.getText()));
            Produtosorcamento produto = (Produtosorcamento) produtojComboBox.getSelectedItem();
            valoresAupair.setProdutosorcamento(produto);
            ValorAupairController valorAupairController = new ValorAupairController();
            valorAupairController.salvar(valoresAupair);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, msg);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

