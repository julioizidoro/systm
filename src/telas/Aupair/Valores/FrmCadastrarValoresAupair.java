    public void iniciaizarDados(){
        if (valoresAupair!=null){
            duracaojTextField.setText(valoresAupair.getDescricao());
            valorNetjTextField.setText(Formatacao.formatarFloatString(valoresAupair.getValornet()));
            valorgrossjTextField.setText(Formatacao.formatarFloatString(valoresAupair.getValorgross()));
            moedasVendasjComboBox.setSelectedItem(valoresAupair.getMoedas());
            produtojComboBox.setSelectedItem(valoresAupair.getProdutosorcamento());
            moedasComissaojComboBox.setSelectedItem(valoresAupair.getMoedas1());
            valorPremiumjTextField.setText(Formatacao.formatarFloatString(valoresAupair.getComissaopremium()));
            valorExpressjTextField.setText(Formatacao.formatarFloatString(valoresAupair.getComissaoexpress()));
        }else {
            valoresAupair = new Valoresaupair();
        }
    }
    
    public String validarDados(){
        String msg ="";
        if (duracaojTextField.getText().length()==0){
            msg = msg + "Campo Período não preenchido\r\n";
        }
        if (valorNetjTextField.getText().length()==0){
            msg = msg + "Campo valor não preenchido\r\n";
        }
        if (valorPremiumjTextField.getText().length()==0){
            msg = msg + "Campo comissão premium não preenchido\r\n";
        }
        if (valorExpressjTextField.getText().length()==0){
            msg = msg + "Campo comissão express não preenchido\r\n";
        }
        if (fornecedorCidade==null){
            msg = msg + "Fornecedor não informada\r\n";
        }
        return msg;
    }
    
    public void carregarComboBox(){
        ProdutoOrcamentoController produtoOrcamentoController = new ProdutoOrcamentoController();
        List<Produtosorcamento> listaProdutos = produtoOrcamentoController.listarProdutoOrcamento("");
        if (listaProdutos!=null){
            produtojComboBox = Formatacao.preencherComobox(listaProdutos, produtojComboBox, false, null);
        }
        CambioController cambioController = new CambioController();
        List<Moedas> listaMoedas = cambioController.listaMoedas();
        moedasVendasjComboBox = Formatacao.preencherComobox(listaMoedas, moedasVendasjComboBox, false, null);
        moedasComissaojComboBox = Formatacao.preencherComobox(listaMoedas, moedasComissaojComboBox, false, null);
    }
    
    public void limitarJText(){
        duracaojTextField.setDocument(new LimiteTextoJedit(50));
    }
