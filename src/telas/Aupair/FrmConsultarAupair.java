    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        if (usuarioLogadoBean.getAcesso().getAupairExcluir() == 1) {
            int linha = vendasCursojTable.getSelectedRow();
            if (linha >= 0) {
                boolean resultado = JOptionPane.showConfirmDialog(null, "Confirma Cancelamento?", "Cancelar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0;
                if (resultado) {
                    int idCliente = listaVendas.get(linha).getIdcliente();
                    int idvenda = listaVendas.get(linha).getIdVendas();
                    String curso = listaVendas.get(linha).getDescricao();
                    String pais = listaVendas.get(linha).getPaisinteresse();
                    String motivo = JOptionPane.showInputDialog("Motivo do Cancelamento");
                    CancelarVenda(listaVendas.get(linha).getIdVendas(), listaVendas.get(linha).getNomeCliente(), motivo, listaVendas.get(linha).getInicio());
                    JOptionPane.showMessageDialog(rootPane, "Ficha de Au Pair Cancelada");
                    new FrmCancelamento(usuarioLogadoBean, idvenda, idCliente, curso);
                    carregarModelVendas();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Acesso Negado");
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

