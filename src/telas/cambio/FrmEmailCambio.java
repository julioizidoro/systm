    public void iniciarlizar(){
        ParametrosProdutosController parametrosProdutosController = new ParametrosProdutosController();
        parametros = parametrosProdutosController.consultar();
        if (parametros!=null){
            email01jTextField.setText(parametros.getEmailCambio01());
            email02jTextField.setText(parametros.getEmailCambio02());
            email03jTextField.setText(parametros.getEmailCambio03());
            email01jTextField.selectAll();
            email01jTextField.requestFocus();
        }
    }


