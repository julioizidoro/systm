    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String msg = validarDados();
        if (msg.length() < 5) {
            valoreshighschool.setDuracao(duracaojComboBox.getSelectedItem().toString());
            valoreshighschool.setInicio(iniciojTextField.getText());
            Moedas moeda = (Moedas) moedasVendasjComboBox.getSelectedItem();
            valoreshighschool.setMoedas(moeda);
            moeda = (Moedas) moedasComissaojComboBox.getSelectedItem();
            valoreshighschool.setMoedas1(moeda);
            valoreshighschool.setFornecedor(fornecedorCidade.getFornecedor());
            valoreshighschool.setFornecedorcidade(fornecedorCidade);
            valoreshighschool.setComissaoexpress(Formatacao.formatarStringfloat(valorExpressjTextField.getText()));
            valoreshighschool.setComissaopremium(Formatacao.formatarStringfloat(valorPremiumjTextField.getText()));
            valoreshighschool.setValornet(Formatacao.formatarStringfloat(valornetjTextField.getText()));
            valoreshighschool.setValorgross(Formatacao.formatarStringfloat(valorgrossjTextField.getText()));
            Filtroorcamentoproduto  filtroorcamentoproduto = ((Filtroorcamentoproduto) produtojComboBox.getSelectedItem());
            valoreshighschool.setProdutosorcamento(filtroorcamentoproduto.getProdutosorcamento());
            valoreshighschool.setDatainicio(dataInicioDateChooser.getDate());
            ValorHighSchoolController valorHighSchoolController = new ValorHighSchoolController();
            valorHighSchoolController.salvar(valoreshighschool);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, msg);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

