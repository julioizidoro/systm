    public void iniciaizarDados(){
        if (valoreshighschool!=null){
            if (valoreshighschool.getFornecedorcidade()!=null){
                fornecedorCidade = valoreshighschool.getFornecedorcidade();
                fornecedorjTextField.setText(fornecedorCidade.getFornecedor().getNome());
            }
            iniciojTextField.setText(valoreshighschool.getInicio());
            duracaojComboBox.setSelectedItem(valoreshighschool.getDuracao());
            valornetjTextField.setText(Formatacao.formatarFloatString(valoreshighschool.getValornet()));
            valorgrossjTextField.setText(Formatacao.formatarFloatString(valoreshighschool.getValorgross()));
            moedasVendasjComboBox.setSelectedItem(valoreshighschool.getMoedas());
            moedasComissaojComboBox.setSelectedItem(valoreshighschool.getMoedas1());
            valorExpressjTextField.setText(Formatacao.formatarFloatString(valoreshighschool.getComissaoexpress()));
            valorPremiumjTextField.setText(Formatacao.formatarFloatString(valoreshighschool.getComissaopremium()));
            dataInicioDateChooser.setDate(valoreshighschool.getDatainicio());
            produtojComboBox.setSelectedItem(valoreshighschool.getProdutosorcamento().getFiltroorcamentoprodutoList().get(0));
        }else {
            valoreshighschool = new Valoreshighschool();
            valoreshighschool.setSituacao("Ativo");
        }
    }
    
    public String validarDados(){
        String msg ="";
        if (iniciojTextField.getText().length()==0){
            msg = msg + "Campo duração não preenchido\r\n";
        }
        if (valornetjTextField.getText().length()==0){
            msg = msg + "Campo valor não preenchido\r\n";
        }
        if (fornecedorCidade==null){
            msg = msg + "Fornecedor não informada\r\n";
        }
        if(dataInicioDateChooser==null){
            msg = msg + "Data de início não informada\r\n";
        }
        return msg;
    }
    
    public void carregarComboBox() {
        FiltroOrcamentoProdutoController filtroOrcamentoProdutoController = new FiltroOrcamentoProdutoController();
        List<Filtroorcamentoproduto> listaProdutosFiltro = filtroOrcamentoProdutoController.pesquisar(usuarioLogadoBean.getParametrosprodutos().getHighSchool());
        produtojComboBox = Formatacao.preencherComobox(listaProdutosFiltro, produtojComboBox, false, null);
        CambioController cambioController = new CambioController();
        List<Moedas> listaMoedas = cambioController.listaMoedas();
        moedasVendasjComboBox = Formatacao.preencherComobox(listaMoedas, moedasVendasjComboBox, false, null);
        moedasComissaojComboBox = Formatacao.preencherComobox(listaMoedas, moedasComissaojComboBox, false, null);
    }
    
