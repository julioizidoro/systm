    private void confirmajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmajButtonActionPerformed
        Cotacaopacotes cotacaoPacotes = new Cotacaopacotes();
        cotacaoPacotes.setDataSolicitacao(new Date());
        cotacaoPacotes.setHoraSolicitacao(Formatacao.foramtarHoraString());
        cotacaoPacotes.setOrigemCliente(origemClientejTextField.getText());
        cotacaoPacotes.setCliente(nomeClientejTextField.getText());
        cotacaoPacotes.setDataIda(dataIdajDateChooser.getDate());
        cotacaoPacotes.setDataRetorno(dataRetornojDateChooser.getDate());
        cotacaoPacotes.setOrigram(origemjTextField.getText());
        cotacaoPacotes.setDestino(destinojTextField.getText());
        if (pacoteCompletojCheckBox.isSelected()){
            cotacaoPacotes.setTipoPacote("C");
        }else cotacaoPacotes.setTipoPacote("T");
        if (adljTextField.getText().length()>0){
            cotacaoPacotes.setNumeroAdultos(Integer.parseInt(adljTextField.getText()));
        }
        if (chd5jTextField.getText().length()>0){
            cotacaoPacotes.setNumeroCriancas5(Integer.parseInt(chd5jTextField.getText()));
        }
        if (chd10jTextField.getText().length()>0){
            cotacaoPacotes.setNumeroCriancas10(Integer.parseInt(chd10jTextField.getText()));
        }
        if (infjTextField.getText().length()>0){
            cotacaoPacotes.setNumeroBebes(Integer.parseInt(infjTextField.getText()));
        }
        cotacaoPacotes.setTipoHotel(categoriajComboBox.getSelectedItem().toString());
        if (duplojCheckBox.isSelected()){
            cotacaoPacotes.setTipoApartamento("Duplo");
        }
        if (triplojCheckBox.isSelected()){
            cotacaoPacotes.setTipoApartamento("Triplo");
        }
        if (quadruplojCheckBox.isSelected()){
            cotacaoPacotes.setTipoApartamento("Quadruplo");
        }
        if (casaljCheckBox.isSelected()){
            cotacaoPacotes.setTipoApartamento("Casal");
        }
        if (twinjCheckBox.isSelected()){
            cotacaoPacotes.setTipoApartamento("Twin");
        }
        if (segurojCheckBox.isSelected()){
            cotacaoPacotes.setSeguro("X");
        }else cotacaoPacotes.setSeguro("");
        if (carrojCheckBox.isSelected()){
            cotacaoPacotes.setCarro("X");
            cotacaoPacotes.setLocalRetiradaCarro(retiradajTextField.getText());
            cotacaoPacotes.setLocalEntregaCarro(entregajTextField.getText());
        }else {
            cotacaoPacotes.setCarro("");
            cotacaoPacotes.setLocalRetiradaCarro("");
            cotacaoPacotes.setLocalEntregaCarro("");
        }
        if (valorjTextField.getText().length()>0){
            cotacaoPacotes.setValorInvestimento(Formatacao.formatarStringfloat(valorjTextField.getText()));
        }
        cotacaoPacotes.setObservacao(observacaojTextArea.getText());
        cotacaoPacotes.setSituacao("Pendente");
        cotacaoPacotes.setUsuario(usuarioLogadoBean.getUsuario().getIdusuario());
        cotacaoPacotes.setUnidadenegocio(usuarioLogadoBean.getUnidadeNegocio().getIdunidadeNegocio());
        CotacaoPacotesController cotacaoPacotesController = new CotacaoPacotesController();
        cotacaoPacotes = cotacaoPacotesController.salvar(cotacaoPacotes);
        JOptionPane.showMessageDialog(rootPane, "Cotação Salva e Enviada para Consultor");
        emitirEmailGerencial(cotacaoPacotes);
        this.dispose();

    }//GEN-LAST:event_confirmajButtonActionPerformed

