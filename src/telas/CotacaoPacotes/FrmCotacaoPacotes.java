public void emitirEmailGerencial(Cotacaopacotes cotacaoPacotes) {
        String texto = "";
        texto = "Nova Cotação";
        String tipoPacote;
        if (cotacaoPacotes.getTipoPacote().equalsIgnoreCase("C")){
            tipoPacote = "Pacote Completo";
        }else tipoPacote = "Somente Terrestre";
        Formatacao.emitirEmailCotacaoPacotes(usuarioLogadoBean.getUnidadeNegocio().getNomeFantasia(),
                usuarioLogadoBean.getUsuario().getNome(), texto, tipoPacote);
    }

