    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        int linha = cotacaojTable.getSelectedRow();
        if (linha>=0){
            boolean resultado = JOptionPane.showConfirmDialog(null, "Confirma Cancelamento?", "Cancelar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0;
            if (resultado) {
                CotacaoPacotesController cotacaoPacotesController = new CotacaoPacotesController();
                Cotacaopacotes cotacaopacotes = cotacaoPacotesController.consultar(listaCotacao.get(linha).getIdcotacaoPacotes());
                if (cotacaopacotes!=null){
                    cotacaopacotes.setSituacao("Cancelada");
                    cotacaoPacotesController.salvar(cotacaopacotes);
                    JOptionPane.showMessageDialog(rootPane, "Cotação Cancelada");
                }
                carregarModel(gerarConsulta());
            }
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

