    public void carregarModel(String sql){
        CotacaoPacotesController cotacaoPacotesController = new CotacaoPacotesController();
        listaCotacao = cotacaoPacotesController.lista(sql);
        if (listaCotacao==null){
            listaCotacao = new ArrayList<Viewconsultacotacaopacote>();
        }
        modelCotacao = new ConsultaCotacaoPacotesTableModel(listaCotacao);
        cotacaojTable.setModel(modelCotacao);
        
        TableColumnModel ColumnModel = cotacaojTable.getColumnModel();  
        JTableRenderer renderer = new JTableRenderer();
        cotacaojTable.getColumnModel().getColumn(0).setCellRenderer(renderer);
        cotacaojTable.getColumnModel().getColumn(0).setPreferredWidth(80);
        cotacaojTable.getColumnModel().getColumn(1).setPreferredWidth(100);
        cotacaojTable.getColumnModel().getColumn(2).setPreferredWidth(100);
        cotacaojTable.getColumnModel().getColumn(3).setPreferredWidth(300);
        cotacaojTable.getColumnModel().getColumn(4).setPreferredWidth(300);
        cotacaojTable.getColumnModel().getColumn(5).setPreferredWidth(200);
        cotacaojTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        cotacaojTable.repaint();
    }
    
    public String gerarConsulta(){
        String sql;
        if (usuarioLogadoBean.getUsuario().getTipo().equalsIgnoreCase("Gerencail")){
            sql = "Select v from Viewconsultacotacaopacote v where v.situacao='Pendente'";
        }else sql = "Select v from Viewconsultacotacaopacote v where v.situacao='Pendente' and v.idunidadeNegocio=" + usuarioLogadoBean.getUnidadeNegocio().getIdunidadeNegocio();
        return sql;
    }
    
    public void imprimirCotacao() {
        int linha = cotacaojTable.getSelectedRow();
        if (linha >= 0) {
            String url = ("telas/CotacaoPacotes/Relatorios/cotacaoPacotes.jasper");
            Map parameters = new HashMap();
            try {
                parameters.put("idPacotes", listaCotacao.get(linha).getIdcotacaoPacotes());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Não foi possível gerar o relatório " + ex);
                ex.printStackTrace();
            }
            new relatoriosJasper(url, parameters);
        }else JOptionPane.showMessageDialog(rootPane, "Selecionar um cotação");
    }


