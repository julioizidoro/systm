    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String msg = validarDados();
        if (msg.length()<5){
            if (pais==null){
                pais = new Pais();
            }
            pais.setNome(nomejTextField.getText());
            pais.setSigla(siglajTextField.getText());
            pais.setMoedas((Moedas) moedajComboBox.getSelectedItem());
            PaisController paisController = new PaisController();
            paisController.salvar(pais);
            JOptionPane.showMessageDialog(rootPane, "País Salvo com Sucesso");
            telaPais.setModelPais();
            this.dispose();
        }else JOptionPane.showMessageDialog(rootPane, msg);
    }//GEN-LAST:event_jButton1ActionPerformed

