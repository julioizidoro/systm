    public void preencherComboMoeda(){
        CambioController  cambioController = new CambioController();
        List<Moedas> listaMoedas = cambioController.listaMoedas();
        if (listaMoedas!=null){
            moedajComboBox = Formatacao.preencherComobox(listaMoedas, moedajComboBox, true,  "Moeda");
        }
    }
    
    public void preencherCampos(){
        nomejTextField.setText(pais.getNome());
        siglajTextField.setText(pais.getSigla());
        moedajComboBox.setSelectedItem(pais.getMoedas());
    }
    
    public String validarDados(){
        String msg = " ";
        if (moedajComboBox.getSelectedItem().toString().equalsIgnoreCase("Moeda")){
           msg = msg + "Selecione a Moeda\n\r";
        }
        if (nomejTextField.getText().length()==0){
            msg = msg + "Nome do País obrigatório\n\r";
        }
        if (nomejTextField.getText().length()==0){
            msg = msg + "Sigla do País obrigatório\n\r";
        }
        return msg;
    }
