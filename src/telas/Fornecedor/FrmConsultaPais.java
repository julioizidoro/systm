    public void setModelPais(){
        PaisController paisController = new PaisController();
        listaPais = paisController.listar(nomejTextField.getText());
        if (listaPais==null){
            listaPais = new ArrayList<Pais>();
        }
        modelPais = new ConsultaPaisTableModel(listaPais);
        paisjTable.setModel(modelPais);
        paisjTable.getColumnModel().getColumn(0).setPreferredWidth(250);
        paisjTable.getColumnModel().getColumn(1).setPreferredWidth(80);
        paisjTable.getColumnModel().getColumn(2).setPreferredWidth(100);
        paisjTable.repaint();
    }
    
    public void setModelCidade(Pais pais){
        List<Cidade> listaCidade = pais.getCidadeList();
        if (listaCidade==null){
            listaCidade = new ArrayList<Cidade>();
        }
        modelCidade = new ConsultaCidadeTableModel(listaCidade);
        cidadejTable.setModel(modelCidade);
        cidadejTable.getColumnModel().getColumn(0).setPreferredWidth(300);
        cidadejTable.repaint();
    }

