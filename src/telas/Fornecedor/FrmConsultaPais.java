    private void AdicionarjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdicionarjButton1ActionPerformed
        int linha = paisjTable.getSelectedRow();
        if (linha >= 0) {
            Pais pais = listaPais.get(linha);
            if (pais != null) {
                String novaCidade = JOptionPane.showInputDialog("Nome da Cidade");
                if (novaCidade.length() > 2) {
                    Cidade cidade = new Cidade();
                    cidade.setNome(novaCidade);
                    cidade.setPais(pais);
                    CidadeController cidadeController = new CidadeController();
                    cidade = cidadeController.salvar(cidade);
                    JOptionPane.showMessageDialog(rootPane, "Cidade cadastrada com sucesso");
                    setModelPais();
                }
            }
        }
    }//GEN-LAST:event_AdicionarjButton1ActionPerformed

