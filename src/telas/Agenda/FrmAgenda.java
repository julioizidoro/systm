    public void carregarComboBox(){
        carAgrada=true;
        String data = Formatacao.ConvercaoDataSql(new Date());
        String ano = data.substring(0, 4);
        String mes = data.substring(5,7);
        String nomeMes = Formatacao.nomeMes(Integer.parseInt(mes));
        anojComboBox.setSelectedItem(ano);
        mesjComboBox.setSelectedItem(nomeMes);
        if (carAgrada){
            carregarModelAgenda();
        }
    }
    
    public void carregarModelAgenda(){
        zerarAgenda();
        carAgrada = false;
        listaAgenda = new ArrayList<AgendaBean>();
        int mes = Formatacao.retornaNumeroMes(mesjComboBox.getSelectedItem().toString());
        String dataFinal = Formatacao.retornaDataFinal(mes);
        int diafinal = Integer.parseInt(dataFinal.substring(3, 5));
        for(int i=0;i<diafinal;i++){
            int dia = i+1;
            String dataString = "";
            if (dia<10){
                dataString = "0" + String.valueOf(dia);
            }else{
                dataString = String.valueOf(dia);
            }
            if (mes<10){
                dataString = dataString + "/" + "0" + mes + "/" + anojComboBox.getSelectedItem().toString();
            }else dataString = dataString + "/" +  mes + "/" + anojComboBox.getSelectedItem().toString();
            Date data = Formatacao.ConvercaoStringData(dataString);
            int diaSemana = Formatacao.diaSemana(data);
            AgendaBean agendaBean = new AgendaBean();
            agendaBean.setData(data);
            agendaBean.setDia(dia);
            agendaBean.setDiaSemana(diaSemana);
            agendaBean.setListaEventos(carregarViewagenda(data));
            listaAgenda.add(agendaBean);
        }
        linha=1;
        for(int i=0;i<listaAgenda.size();i++){
            if (linha==1){
                carregarLinha01(i);
            }else if (linha==2){
                carregarLinha02(i);
            }else if (linha==3){
                carregarLinha03(i);
            } else if (linha==4){
                carregarLinha04(i);
            } else if (linha==5){
                carregarLinha05(i);
            } else if (linha==6){
                carregarLinha06(i);
            }
        }
    }
    
