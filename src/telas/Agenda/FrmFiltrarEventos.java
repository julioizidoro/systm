    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int consultor =0;
        if (consultorjComboBox.getSelectedItem().toString().equalsIgnoreCase("Consultor")){
            consultor=0;
        }else {
            Usuario usuario = (Usuario) consultorjComboBox.getSelectedItem();
            consultor = usuario.getIdusuario();
        }
        int unidade =0;
        if (UnidadejComboBox.getSelectedItem().toString().equalsIgnoreCase("Unidade Neg√≥cio")){
            unidade=0;
        }else {
            Unidadenegocio unidadenegocio= (Unidadenegocio) UnidadejComboBox.getSelectedItem();
            unidade = unidadenegocio.getIdunidadeNegocio();
        }
        this.telaAgenda.setFiltro(consultor, unidade, horajFormattedTextField.getText());
        this.dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

