    public void iniciarlizar(){
        UsuarioController usuarioController = new UsuarioController();
        List<Usuario> listaUsuario = usuarioController.listaUsuario();
        if (listaUsuario==null){
            listaUsuario = new ArrayList<Usuario>();
        }
        consultorjComboBox = Formatacao.preencherComobox(listaUsuario, consultorjComboBox, true, "Consultor");
        UnidadeNegocioController unidadeNegocioController = new UnidadeNegocioController();
        List<Unidadenegocio> listaUnidadeNegocio = unidadeNegocioController.listar();
        if (listaUnidadeNegocio==null){
            listaUnidadeNegocio = new ArrayList<Unidadenegocio>();
        }
        UnidadejComboBox = Formatacao.preencherComobox(listaUnidadeNegocio, UnidadejComboBox, true, "Unidade Neg√≥cio");
        try {           
            horajFormattedTextField.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("##:##")));
        } catch (ParseException ex) {
            Logger.getLogger(FrmFiltrarEventos.class.getName()).log(Level.SEVERE, null, ex);
        }
        horajFormattedTextField.setText( "00:00");
    }


