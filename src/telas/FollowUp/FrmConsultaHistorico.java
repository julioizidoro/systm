    public void carregarModelHistorico(){
        String sql="";
        if (usuarioLogadoBean.getUsuario().getTipo().equalsIgnoreCase("Gerencial")){
            sql = "Select a From Viewconsultacontroleloja a where a.clienteIdcliente=" + cliente.getIdcliente();
        } else {
            sql = "Select a From Viewconsultacontroleloja a where unidadeNegocioidUnidadeNegocio=" + usuarioLogadoBean.getUnidadeNegocio().getIdunidadeNegocio() +
                    " and a.clienteIdcliente="+ cliente.getIdcliente();
        }        
        ControleLojaController  controleLojaController = new ControleLojaController();
        listaHistorico = controleLojaController.listaFollowup(sql);
        if (listaHistorico==null){
            listaHistorico = new ArrayList<Viewconsultacontroleloja>();
        }
        modelHistorico = new ConsultaHistoricoTableModel(listaHistorico);
        historicojTable.setModel(modelHistorico);
        historicojTable.getColumnModel().getColumn(0).setPreferredWidth(30);
        historicojTable.getColumnModel().getColumn(1).setPreferredWidth(30);
        historicojTable.getColumnModel().getColumn(2).setPreferredWidth(200);
        historicojTable.getColumnModel().getColumn(3).setPreferredWidth(100);
        historicojTable.repaint();
    }

    @Override
    public void setModelHistorico(Followup followup) {
        this.followup = followup;
        carregarModelHistorico();
        this.telaFollowup.setModel();
    }

