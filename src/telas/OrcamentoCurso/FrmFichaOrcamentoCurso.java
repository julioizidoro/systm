    public void carregarInicializacao(int idVenda, boolean duplicar){
        carregarModelOrcamentoProduto();
        carregarListaProdutos(1);
        carregarComboMoedas();
        if (usuarioLogadoBean.getUnidadeNegocio().getDigitosTelefone()==9){
            foneResidencialjCheckBox.setText("8-Fone Residencial");
            foneCelularjCheckBox.setText("8-Fone Celular");
        }else {
            foneResidencialjCheckBox.setText("9-Fone Residencial");
            foneCelularjCheckBox.setText("9-Fone Celular");
        }
        if (idVenda>0){
            if (!duplicar){
                carregarObjetos(idVenda);
            }else if (duplicar){
                duplicarOrcamento(idVenda);
            }
        }else{
            Parametrosprodutos parametrosprodutos = new Parametrosprodutos();
            ParametrosProdutosController parametrosProdutosController = new ParametrosProdutosController();
            parametrosprodutos = parametrosProdutosController.consultar();
            ProdutoOrcamentoController produtoorcamentoController = new ProdutoOrcamentoController();
            Produtosorcamento produtosorcamento = produtoorcamentoController.consultar(parametrosprodutos.getPassagemTaxaTM());
            ProdutoOrcamentoCursoBean pob = new ProdutoOrcamentoCursoBean();
            pob.setIdProdutoOrcamento(produtosorcamento.getIdprodutosOrcamento());
            pob.setDescricaoProdutoOrcamento(produtosorcamento.getDescricao());
            pob.setIdProdutoOrcamento(produtosorcamento.getIdprodutosOrcamento());
            pob.setValorMoedaEstrangeira(0.0f);
            pob.setValorMoedaReal(parametrosprodutos.getValorTaxaTM());
            pob.setApagar(false);
            pob.setNovo(true);
            listaProdutoOrcamentoBean.add(pob);
            consultaCambio=true;
        }
    }
    
