    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        int linha = orcamentoCursojTable.getSelectedRow();
        if (linha>=0){
            boolean resultado = JOptionPane.showConfirmDialog(null, "Confirma Cancelamento do Orçamento?", "Excluir", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0;
            if (resultado) {
                ModeloOrcamentoCursoController modeloOrcamentoCursoController = new ModeloOrcamentoCursoController();
                Modeloorcamentocurso modeloorcamentocurso = modeloOrcamentoCursoController.consultar(listaModelo.get(linha).getIdorcamentoCurso());
                if (modeloorcamentocurso!=null){
                    int idUnidade = usuarioLogadoBean.getUnidadeNegocio().getIdunidadeNegocio();
                    int idUnidadeOrcamento = modeloorcamentocurso.getUnidadenegocio();
                    if (idUnidade == idUnidadeOrcamento){
                        modeloorcamentocurso.setSituacao("Cancelado");
                        modeloOrcamentoCursoController.salvar(modeloorcamentocurso);
                        carregarModelVendas();
                    }else JOptionPane.showMessageDialog(rootPane, "Este modelo não pertence a sua Unidade");
                }
            }
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

