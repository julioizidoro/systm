    public void carregarModelVendas(){
        try {
            ConexaoSingleton.desconectar();
        } catch (SQLException ex) {
            Logger.getLogger(FrmConsultarCursos.class.getName()).log(Level.SEVERE, null, ex);
        }
        String sql = null;
        if (usuarioLogadoBean.getUsuario().getTipo().equalsIgnoreCase("Gerencial")){
            sql = "select v from Viewmodeloorcamento v where v.situacao='Ativo' order by v.nome";
        }else {
            sql = "select v from Viewmodeloorcamento v ";
            if (matrizjCheckBox.isSelected()){
                sql = sql + " where idunidadeNegocio=6 or idunidadeNegocio=1 or  idunidadeNegocio=2 or idunidadeNegocio=" + 
                    usuarioLogadoBean.getUnidadeNegocio().getIdunidadeNegocio() + " and v.situacao='Ativo' order by v.nome";
            }else {
                sql = sql + " where idunidadeNegocio=" + 
                    usuarioLogadoBean.getUnidadeNegocio().getIdunidadeNegocio() + " and v.situacao='Ativo' order by v.nome";
            }
            
        }
        ModeloOrcamentoCursoController modeloOrcamentoCursoController = new ModeloOrcamentoCursoController();
        listaModelo = modeloOrcamentoCursoController.listarModeloOrcamentoCurso(sql);
        if (listaModelo==null){
            listaModelo = new ArrayList<Viewmodeloorcamento>();
        }
        modelModelo = new ConsultaModeloOrcamentoCursoTableModel(listaModelo);
        orcamentoCursojTable.setModel(modelModelo);
        orcamentoCursojTable.getColumnModel().getColumn(0).setPreferredWidth(200);
        orcamentoCursojTable.getColumnModel().getColumn(1).setPreferredWidth(100);
        orcamentoCursojTable.getColumnModel().getColumn(2).setPreferredWidth(200);
        orcamentoCursojTable.repaint();
    }

