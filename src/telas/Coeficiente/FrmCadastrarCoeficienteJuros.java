    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String msg = validarDados();
        if (msg.length() > 5) {
            JOptionPane.showMessageDialog(rootPane, msg);
        } else {

            if (coeficiente == null) {
                coeficiente = new Coeficientejuros();
            }
            coeficiente.setNumeroParcelas(Integer.parseInt(numeroParcelasjTextField.getText()));
            coeficiente.setCoeficiente(Formatacao.formatarStringDouble(coeficientejTextField.getText()));
            CoeficienteJurosController coeficienteJurosController = new CoeficienteJurosController();
            coeficiente = coeficienteJurosController.salvar(coeficiente);
            telaConsulta.setModel();
            this.dispose();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

