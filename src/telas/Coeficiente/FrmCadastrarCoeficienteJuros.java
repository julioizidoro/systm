    public String validarDados(){
        String msg="";
        if (numeroParcelasjTextField.getText().length()<=0){
            msg = msg + "NÂº de parcelas em branco\b\n";
        }
        if (coeficientejTextField.getText().length()<=0){
            msg = msg + "Valor do coeficiente em branco";
        }
        return msg;
    }
