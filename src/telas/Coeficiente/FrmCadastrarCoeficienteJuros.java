    private void coeficientejTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_coeficientejTextFieldKeyTyped
        if (evt.getKeyChar()=='.'){
            evt.setKeyChar(',');
        }
        String caracteres = "0987654321,-";
        if (evt.getKeyChar() != '\b') {
            if (!caracteres.contains(evt.getKeyChar() + "")) {
                evt.consume();
            }
        }
    }//GEN-LAST:event_coeficientejTextFieldKeyTyped

