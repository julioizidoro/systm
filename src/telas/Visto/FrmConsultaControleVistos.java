    private void imprimirjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imprimirjButton2ActionPerformed
        if (usuarioLogadoBean.getAcesso().getPcvfinalizar()==1){
            int linha =vendasCursojTable.getSelectedRow();
            if (linha>=0){
                VistosController vistosController = new VistosController();
                Controlevistos controlevistos =  vistosController.consultarControleVistos(listaVendas.get(linha).getIdvendas());
                if (controlevistos!=null){
                    controlevistos.setFinalizado("Finalizado");
                    vistosController.salvar(controlevistos);
                }
                Vistos visto = vistosController.consultarVistos(listaVendas.get(linha).getIdvendas());
                if (visto!=null){
                    visto.setControle("Finalizado");
                    vistosController.salvar(visto);
                }
                carregarModelVendas();
            }
        }else JOptionPane.showMessageDialog(rootPane, "Acesso Negado");
    }//GEN-LAST:event_imprimirjButton2ActionPerformed

