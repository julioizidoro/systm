    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String msg = validarDados();
        if (msg.length() < 5) {
            valoresVistos.setCategoria((String) categoriajComboBox.getSelectedItem());
            valoresVistos.setValornet(Formatacao.formatarStringfloat(valornetjTextField.getText()));
            valoresVistos.setValorgross(Formatacao.formatarStringfloat(valorgrossjTextField.getText()));
            valoresVistos.setComissaopremium(Formatacao.formatarStringfloat(valorpremiumjTextField.getText()));
            valoresVistos.setComissaoexpress(Formatacao.formatarStringfloat(valorexpressjTextField.getText()));
            valoresVistos.setSituacao("ATIVO");
            VistosController VistosController = new VistosController();
            VistosController.salvar(valoresVistos);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, msg);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

