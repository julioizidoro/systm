    public void iniciaizarDados(){
        if (valoresVistos!=null){
            paisjTextField.setText(valoresVistos.getFornecedorcidade().getCidade().getPais().getNome());
            categoriajComboBox.setSelectedItem(valoresVistos.getCategoria());
            valornetjTextField.setText(Formatacao.formatarFloatString(valoresVistos.getValornet()));
            valornetjTextField.setText(Formatacao.formatarFloatString(valoresVistos.getValorgross()));
            valorpremiumjTextField.setText(Formatacao.formatarFloatString(valoresVistos.getComissaopremium()));
            valorexpressjTextField.setText(Formatacao.formatarFloatString(valoresVistos.getComissaoexpress()));
        }else {
            valoresVistos = new Valoresvistos();
        }
    }
    
    public String validarDados(){
        String msg ="";
        if (valoresVistos.getFornecedorcidade()==null){
            msg = msg + "País não selecionado\r\n";
        }
        if (valornetjTextField.getText().length()==0){
            msg = msg + "Campo valor NET não preenchido\r\n";
        }
        if (valorgrossjTextField.getText().length()==0){
            msg = msg + "Campo valor Gross não preenchido\r\n";
        }
        if (valorpremiumjTextField.getText().length()==0){
            msg = msg + "Campo comissão premium não preenchido\r\n";
        }
        if (valornetjTextField.getText().length()==0){
            msg = msg + "Campo valor NET não preenchido\r\n";
        }
        if (categoriajComboBox.getSelectedItem().toString().equalsIgnoreCase("SELECIONE CATEGORIA")){
            msg = msg + "Categoria não Selecionada\r\n";
        }
        return msg;
    }

    @Override
    public void setFornecedorCidade(Fornecedorcidade fornecedorcidade) {
        if (fornecedorcidade!=null){
            valoresVistos.setFornecedorcidade(fornecedorcidade);
            paisjTextField.setText(fornecedorcidade.getCidade().getPais().getNome());
        }
    }
