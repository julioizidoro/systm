    private void localiarInvoice() {
        InvoicesController invoicesController = new InvoicesController();
        invoice = invoicesController.consultarVenda(idVenda, idProduto, idControle);
        if (invoice != null) {
            if (!invoice.getSituacao().equalsIgnoreCase("cancelada")) {
                dataPrevistaPagtojDateChooser.setDate(invoice.getDataPrevistaPagamento());
                valorPrevistoPagtojTextField.setText(Formatacao.formatarFloatString(invoice.getValorPrevistoInvoice()));
                valorPagtojTextField.setText(Formatacao.formatarFloatString(invoice.getValorPagamentoInvoice()));
                valorcreditojTextField.setText(Formatacao.formatarFloatString(invoice.getValorcredito()));
                dataRecebimentoComporvantejDateChooser.setDate(invoice.getDatarecebimentocomprovante());
                observacaoCreditojTextField.setText(invoice.getObscredito());
                prioridadejComboBox.setSelectedItem(invoice.getPrioridade());
            }else {
                JOptionPane.showMessageDialog(rootPane, "Invoice Cancelada");
                this.dispose();
            }

        }
    }
    
