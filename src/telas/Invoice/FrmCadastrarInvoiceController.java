    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (invoice==null){
            invoice = new Invoices();
            invoice.setCambioPagamento(0.0f);
            invoice.setGanhoCambio(0.0f);
            invoice.setValorPago(0.0f);
            invoice.setVendas(idVenda);
            invoice.setGanhoperda(0.0f);
            invoice.setControle(idControle);
            invoice.setProdutos(idProduto);
        }
        if (valorPagtojTextField.getText().length()>0){
            invoice.setValorPagamentoInvoice(Formatacao.formatarStringfloat(valorPagtojTextField.getText()));
        }else invoice.setValorPagamentoInvoice(0.0f);
        if (valorPrevistoPagtojTextField.getText().length()>0){
            invoice.setValorPrevistoInvoice(Formatacao.formatarStringfloat(valorPrevistoPagtojTextField.getText()));
        }else invoice.setValorPrevistoInvoice(0.0f);
        if (valorcreditojTextField.getText().length()>0){
            invoice.setValorcredito(Formatacao.formatarStringfloat(valorcreditojTextField.getText()));
        }else invoice.setValorcredito(0.0f);
        invoice.setObscredito(observacaoCreditojTextField.getText());
        invoice.setPrioridade(prioridadejComboBox.getSelectedItem().toString());
        invoice.setDataPrevistaPagamento(dataPrevistaPagtojDateChooser.getDate());
        invoice.setDatarecebimentocomprovante(dataRecebimentoComporvantejDateChooser.getDate());
        invoice.setPrioridade(prioridadejComboBox.getSelectedItem().toString());
        InvoicesController invoicesController = new InvoicesController();
        invoicesController.salvar(invoice);
        this.dispose();
    }//GEN-LAST:event_jButton5ActionPerformed

